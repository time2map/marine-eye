function isoToEmoji(iso2) {
  return [...iso2.toUpperCase()].map(c => String.fromCodePoint(c.charCodeAt(0) + 127397)).join('');
}

export function mmsiToFlag(mmsi) {
  const mid = parseInt(String(mmsi).slice(0, 3), 10);
  const entry = MID[mid];
  if (!entry) return null;
  const [iso2, country] = entry;
  return { emoji: isoToEmoji(iso2), country, iso2 };
}
